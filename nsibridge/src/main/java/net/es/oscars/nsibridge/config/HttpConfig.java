package net.es.oscars.nsibridge.config;


public class HttpConfig {
    protected String url;
    protected String bus;


    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getBus() {
        return bus;
    }

    public void setBus(String bus) {
        this.bus = bus;
    }
}
